public class LinearLoop3 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        final int LIMIT = 25;
        
        for(int count = 1; count <= LIMIT; count++){
            System.out.println(count);
        }
        System.out.println("Done");
    }
    
}
